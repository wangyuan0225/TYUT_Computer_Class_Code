#include <stdio.h>

int main() {
    int T;
    scanf("%d", &T);
    int num[9];
    for (int i = 0; i < T; i++) {
        scanf("%d", &num[i]);
    }
    for (int i = 0; i < num[i]; i++) {
        for (int j = 0; j < 3 * num[i]; j++) {
            for (int p = 0; p < num[i]; p++) {
                printf("HDU");
            }
            printf("\n");
        }
    }
    return 0;
}
